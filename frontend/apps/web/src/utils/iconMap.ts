/**
 * Get icon component by name.
 *
 * @param name Icon name (e.g., "Users", "Calendar")
 * @returns LucideIcon component or undefined if not found
 */
export function getIcon(name: string | undefined): LucideIcon | undefined {
  if (!name) return undefined;
  return iconMap[name];
}

/**
 * Get icon component by name with fallback.
 *
 * @param name Icon name
 * @param fallback Fallback icon component
 * @returns LucideIcon component
 */
export function getIconWithFallback(name: string | undefined, fallback: LucideIcon): LucideIcon {
  if (!name) return fallback;
  return iconMap[name] ?? fallback;
}

export default iconMap;
